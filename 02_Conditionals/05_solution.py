weather = input("How's the weather? ")

if weather == "Sunny":
    print("Go for a walk")
    
elif weather == "Rainy":
    print("Read a book")
    
elif weather == "Snowy":
    print("Make a snowman")
    
elif weather == "Windy":
    print("Fly a kite")
    
else:
    print("You sure about the weather!!!")